package com.example.androidtesting

class Calculator {
    fun addOperation(num1: Int, num2: Int): Int {
        return num1 + num2
    }

    fun subOperation(num1: Int, num2: Int): Int {
        return num1 - num2
    }

    fun mulOperation(num1: Int, num2: Int): Int {
        return num1 * num2
    }

    fun divOperation(num1: Int, num2: Int): Int {
        return num1 / num2
    }
}